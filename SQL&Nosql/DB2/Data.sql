DROP PROCEDURE IF EXISTS Activity_level;
DELIMITER //
CREATE PROCEDURE Activity_level()
BEGIN
      SELECT Authors.fullname,
    CASE
        WHEN
			 COUNT(Owners.a_id) < 1
		THEN 'not active'
        WHEN
            COUNT(Owners.a_id) = 1 or COUNT(Owners.a_id) = 2
        THEN 'low'
        WHEN
			 COUNT(Owners.a_id) > 2 and COUNT(Owners.a_id) <= 5
        THEN 'medium'
        WHEN
			COUNT(Owners.a_id) > 5
        THEN 'high'

END AS activity_level FROM Owners
JOIN Authors ON Authors.id = Owners.a_id
JOIN Posts ON Posts.id = Owners.p_id
GROUP BY Authors.id;
END //
DELIMITER ;
CALL Activity_level();

